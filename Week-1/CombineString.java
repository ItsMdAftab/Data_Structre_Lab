import java.util.Scanner;
class CombineString
{	
	public static void main(String args[])
	{
		Scanner sc = new Scanner(System.in);
		System.out.print("Enter the first string:");
		String k1 = sc.next();
		System.out.print("Enter the Secound String: ");
		String k2 = sc.next();
		System.out.println(k1+k2+k1);
	}
}
	
	
	
	
